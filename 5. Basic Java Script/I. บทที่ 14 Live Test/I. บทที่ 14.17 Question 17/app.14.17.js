const input = 5;

// ลูปนอก (i) ควบคุมจำนวนแถว
for (let i = 1; i <= input; i++) {
    let output = ""; // สร้างตัวแปรว่างไว้เก็บดาวในแต่ละแถว
    
    // ลูปใน (j) ควบคุมจำนวนดาวในแต่ละแถว
    for (let j = 1; j <= i; j++) {
        output += "*"; // เพิ่มดาวเข้าไปตามจำนวนรอบของ j
    }
    
    console.log(output); // พิมพ์ดาวของแถวนั้นๆ ออกมา
}