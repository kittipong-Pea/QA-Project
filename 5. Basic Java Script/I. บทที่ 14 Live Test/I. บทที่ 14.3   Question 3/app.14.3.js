function pow(base, exponent) {
    return base ** exponent;
}

console.log(pow(8, 2));  // 64
console.log(pow(9, 3));  // 729
console.log(pow(7, 5));  // 16807
console.log(pow(6, 6));  // 279936


function pow2(x,y) {
    let result = x ** y ;
    return result   
}

console.log(pow2(8, 2));  // 64
console.log(pow2(9, 3));  // 729
console.log(pow2(7, 5));  // 16807
console.log(pow2(6, 6));  // 279936


